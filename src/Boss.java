public class Boss {
    private int heal;
    private int damage;
    private String defends;

    public int getHeal() {
        return heal;
    }

    public void setHeal(int heal) {
        this.heal = heal;
    }

    public int getDamage() {
        return damage;
    }

    public void setDamage(int damage) {
        this.damage = damage;
    }

    public String getDefends() {
        return defends;
    }

    public void setDefends(String defends) {
        this.defends = defends;
    }
}
